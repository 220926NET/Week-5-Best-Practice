class Pokemon {
  constructor(name, hp, type, number, height, weight, move1, moveText1, move2, moveText2, entry) {
    this.Name = name;
    this.HP = hp;
    this.Type = type;
    this.Number = number;
    this.Height = height;
    this.Weight = weight;
    this.Move1 = move1;
    this.MoveText1 = moveText1;
    this.Move2 = move2;
    this.MoveText2 = moveText2;
    this.Entry = entry;
  }
}